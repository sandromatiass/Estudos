//aqui funciona da seguinte forma  for

// let diz o valor da variavel certo

//  apos a virgula vem a regra do laço, enquanto a idade for menor que 100 multiplique a idade por 2 até chegar
// ao valor mais proximo a 100 mais nunca passa

for(let idade = 2; idade < 100; idade = idade * 2){
    console.log("O número é:" + idade)
}

for(let i = 0; i < 100; i += 5){
    console.log(`A ordem e o seginte ${i}`)
}